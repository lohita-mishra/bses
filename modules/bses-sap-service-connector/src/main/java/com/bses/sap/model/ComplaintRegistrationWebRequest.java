package com.bses.sap.model;

public class ComplaintRegistrationWebRequest {
	
	
	private String key;
	private String area;
	private String callerName;
	private String callerphone;
	private String callerEmail;
	private String address;
	private String faultCat;
	private String faultType;
	private String remarks;
	private String caNo;
	private String source;
	private String poleNum;
	
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getPoleNum() {
		return poleNum;
	}
	public void setPoleNum(String poleNum) {
		this.poleNum = poleNum;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getCallerName() {
		return callerName;
	}
	public void setCallerName(String callerName) {
		this.callerName = callerName;
	}
	public String getCallerphone() {
		return callerphone;
	}
	public void setCallerphone(String callerphone) {
		this.callerphone = callerphone;
	}
	public String getCallerEmail() {
		return callerEmail;
	}
	public void setCallerEmail(String callerEmail) {
		this.callerEmail = callerEmail;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getFaultCat() {
		return faultCat;
	}
	public void setFaultCat(String faultCat) {
		this.faultCat = faultCat;
	}
	public String getFaultType() {
		return faultType;
	}
	public void setFaultType(String faultType) {
		this.faultType = faultType;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getCaNo() {
		return caNo;
	}
	public void setCaNo(String caNo) {
		this.caNo = caNo;
	}
	
	

}
